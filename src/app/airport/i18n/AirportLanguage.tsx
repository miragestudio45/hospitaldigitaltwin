function translateVietnamese(text: string) {
  if (VI[text]) return VI[text];
  let translated = text;
  for (const [source, target] of [...VI_PHRASES].sort((a, b) => b[0].length - a[0].length)) {
    translated = translated.replaceAll(source, target);
  }
  return translated;
}

interface AirportLanguageContextValue {
  language: AirportLanguage;
  setLanguage: (language: AirportLanguage) => void;
  tr: (text: string) => string;
  localizeModule: (module: AirportModuleDefinition) => AirportModuleDefinition;
  localizeSection: (section: AirportSectionDefinition) => AirportSectionDefinition;
}

const AirportLanguageContext = createContext<AirportLanguageContextValue | null>(null);

export function AirportLanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguage] = useState<AirportLanguage>(() => {
    const stored = window.localStorage.getItem("hospital-digital-twin-language");
    return stored === "en" ? "en" : "vi";
  });

  const value = useMemo<AirportLanguageContextValue>(() => {
    const tr = (text: string) => language === "vi" ? translateVietnamese(text) : text;
    const localizeSection = (section: AirportSectionDefinition): AirportSectionDefinition => ({
      ...section,
      label: tr(section.label),
      description: language === "vi" ? (VI_SECTION_DESC[section.id] ?? tr(section.description)) : section.description,
    });
    const localizeModule = (module: AirportModuleDefinition): AirportModuleDefinition => ({
      ...module,
      label: tr(module.label),
      shortLabel: tr(module.shortLabel),
      sections: module.sections.map(localizeSection),
    });
    return {
      language,
      setLanguage: (next) => {
        window.localStorage.setItem("hospital-digital-twin-language", next);
        setLanguage(next);
      },
      tr,
      localizeModule,
      localizeSection,
    };
  }, [language]);

  return <AirportLanguageContext.Provider value={value}>{children}</AirportLanguageContext.Provider>;
}

export function useAirportLanguage() {
  const context = useContext(AirportLanguageContext);
  if (!context) throw new Error("useAirportLanguage must be used inside AirportLanguageProvider");
  return context;
}
